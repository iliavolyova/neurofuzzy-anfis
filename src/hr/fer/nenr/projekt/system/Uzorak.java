package hr.fer.nenr.projekt.system;

public class Uzorak {

	int x;
	int y;
	double f;
	
	public Uzorak(int x, int y, double f) {
		super();
		this.x = x;
		this.y = y;
		this.f = f;
	}
	
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public double getF() {
		return f;
	}
	
	
}
